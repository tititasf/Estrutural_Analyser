import sys
import base64
try:
    import win32crypt
except ImportError:
    win32crypt = None

class DataProtectionService:
    """
    Serviço de Segurança para proteger dados locais usando DPAPI (Windows).
    Os dados encriptados só podem ser descriptografados pelo mesmo usuário no mesmo computador.
    """
    
    @staticmethod
    def protect(data: str) -> str:
        """
        Encripta string usando DPAPI.
        Retorna string em base64.
        """
        if not data:
            return ""
            
        if not win32crypt:
            # Fallback seguro: Em dev sem pywin32, retorne erro ou texto plano (não recomendado para prod)
            # Para este projeto, assumimos que pywin32 existe (AutoCAD requer).
            raise ImportError("pywin32 (win32crypt) is required for secure storage on Windows.")

        data_bytes = data.encode('utf-8')
        # CryptProtectData(data, description, optional_entropy, reserved, prompt_struct, flags)
        encrypted_blob = win32crypt.CryptProtectData(data_bytes, None, None, None, None, 0)
        return base64.b64encode(encrypted_blob).decode('ascii')

    @staticmethod
    def unprotect(encrypted_data: str) -> str:
        """
        Decripta string base64 usando DPAPI.
        """
        if not encrypted_data:
            return ""
            
        if not win32crypt:
            raise ImportError("pywin32 (win32crypt) is required for secure storage on Windows.")

        try:
            encrypted_blob = base64.b64decode(encrypted_data)
            # CryptUnprotectData(data, entropy, reserved, prompt_struct, flags)
            decrypted_blob = win32crypt.CryptUnprotectData(encrypted_blob, None, None, None, 0)
            return decrypted_blob[1].decode('utf-8')
        except Exception as e:
            print(f"Failed to decrypt data: {e}")
            return ""
