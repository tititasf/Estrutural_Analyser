SESSION_FILE = ".session_secure.dat" # Changed extension to denote binary/secure nature

class AuthService:
    """
    Serviço de Domínio para Autenticação.
    Gerencia Login, Logout e Estado da Sessão.
    Implementado como Singleton para manter estado em toda a aplicação.
    """
    _instance = None

    def __new__(cls):
        if cls._instance is None:
            cls._instance = super().__new__(cls)
            cls._instance._initialized = False
        return cls._instance

    def __init__(self):
        if self._initialized:
            return
        self.supabase = SupabaseClient.get_instance().client
        self._current_session: Optional[AuthSession] = None
        self._load_persisted_session()
        self._initialized = True

    def login(self, email: str, password: str, remember: bool = False) -> Optional[AuthSession]:
        """Autentica usuário e retorna sessão tipada"""
        if not self.supabase:
            raise RuntimeError("Supabase client not initialized")

        try:
            # Chama API do Supabase Auth
            response = self.supabase.auth.sign_in_with_password({
                "email": email,
                "password": password
            })
            
            if response.user and response.session:
                # Mapeia resposta bruta para nossos Modelos Atômicos
                # Tenta pegar role de app_metadata (mais seguro) ou user_metadata
                app_meta = getattr(response.user, 'app_metadata', {})
                user_meta = getattr(response.user, 'user_metadata', {})
                
                user_role = app_meta.get('role') or user_meta.get('role')
                
                # Fallback manual para o desenvolvedor ser Admin se não houver role no banco ainda
                if not user_role and email == "thierry.tasf@gmail.com":
                    user_role = "admin"
                
                # Fetch credits from profiles table
                credits_balance = 0
                try:
                    profile_response = self.supabase.table("profiles").select("credits").eq("id", response.user.id).single().execute()
                    if profile_response.data:
                        credits_balance = profile_response.data.get("credits", 0)
                except Exception as ex:
                    print(_get_obf_str(_get_obf_str(_get_obf_str(_get_obf_str(_get_obf_str("credit"))))))

                profile = UserProfile(
                    id=response.user.id,
                    email=email,
                    full_name=user_meta.get('full_name'),
                    role=UserRole(user_role) if user_role in [r.value for r in UserRole] else UserRole.USER,
                    created_at=response.user.created_at,
                    last_login=response.user.last_sign_in_at,
                    credits=credits_balance
                )

                self._current_session = AuthSession(
                    access_token=response.session.access_token,
                    refresh_token=response.session.refresh_token,
                    expires_at=response.session.expires_at or 0,
                    user=profile
                )
                
                if remember:
                    self._persist_session()
                
                return self._current_session
            
        except Exception as e:
            print(f"Login failed: {e}")
            return None
        
        return None

    def sign_up(self, email: str, password: str, full_name: str) -> bool:
        """Registra novo usuário no Supabase"""
        if not self.supabase:
            raise RuntimeError("Supabase client not initialized")
        
        try:
            response = self.supabase.auth.sign_up({
                "email": email,
                "password": password,
                "options": {
                    "data": {
                        "full_name": full_name
                    }
                }
            })
            return response.user is not None
        except Exception as e:
            print(f"Sign up failed: {e}")
            raise e

    def verify_signup(self, email: str, token: str) -> bool:
        """Confirma o cadastro usando o token (OTP) enviado por e-mail"""
        if not self.supabase:
            raise RuntimeError("Supabase client not initialized")
        
        try:
            # Tenta verificar como signup (novo usuário)
            res = self.supabase.auth.verify_otp({
                "email": email,
                "token": token,
                "type": "signup"
            })
            return res.user is not None
        except Exception as e:
            print(f"Verification failed: {e}")
            # Em alguns casos (login magic link), o tipo pode ser diferente, 
            # mas para signup explicito 'signup' é o correto.
            return False

    def resend_confirmation(self, email: str) -> bool:
        """Reenvia o e-mail de confirmação para um usuário não confirmado"""
        if not self.supabase:
            raise RuntimeError("Supabase client not initialized")
        
        try:
            # Type 'signup' is used for new account confirmation
            self.supabase.auth.resend({
                "type": "signup",
                "email": email
            })
            return True
        except Exception as e:
            print(f"Resend failed: {e}")
            return False

    def _persist_session(self):
        """Salva a sessão em um arquivo local encriptado via DPAPI"""
        if self._current_session:
            try:
                json_data = self._current_session.model_dump_json()
                encrypted_data = DataProtectionService.protect(json_data)
                
                with open(SESSION_FILE, "w") as f:
                    f.write(encrypted_data)
            except Exception as e:
                print(f"Erro ao persistir sessão: {e}")

    def _load_persisted_session(self):
        """Tenta carregar sessão salva e decriptar"""
        if os.path.exists(SESSION_FILE):
            try:
                with open(SESSION_FILE, "r") as f:
                    encrypted_data = f.read()
                    
                json_data = DataProtectionService.unprotect(encrypted_data)
                if not json_data:
                    raise ValueError("Decryption returned empty data")

                data = json.loads(json_data)
                self._current_session = AuthSession(**data)
                
                # Verifica se o Supabase ainda reconhece a sessão (refresh se necessário)
                if self.supabase and self._current_session:
                     self.supabase.auth.set_session(self._current_session.access_token, self._current_session.refresh_token)
            except Exception as e:
                print(f"Sessão persistida inválida ou expirada: {e}")
                if os.path.exists(SESSION_FILE):
                    os.remove(SESSION_FILE)

    def logout(self):
        if self.supabase:
            self.supabase.auth.sign_out()
        self._current_session = None
        if os.path.exists(SESSION_FILE):
            os.remove(SESSION_FILE)

    def get_session(self) -> Optional[AuthSession]:
        return self._current_session

    def get_current_user(self) -> Optional[UserProfile]:
        if self._current_session:
            return self._current_session.user
        return None
    
    def is_authenticated(self) -> bool:
        return self._current_session is not None
    
