import math
from enum import Enum
from typing import List, Tuple, Dict, Optional
from shapely.geometry import Polygon, Point
from shapely.affinity import rotate
import numpy as np

class ShapeType(Enum):
    RECTANGULAR = "RETANGULAR"
    CIRCULAR = "CIRCULAR"
    L_SHAPE = "L"
    T_SHAPE = "T"
    U_SHAPE = "U"
    UNKNOWN = "DESCONHECIDO"

class GeometryEngine:
    """
    O Motor Geométrico responsável por classificar formas e mapear lados (A-H)
    seguindo rigorosamente as regras do 'Pillar Compass'.
    """

    @staticmethod
    def classify_shape(vertices: List[Tuple[float, float]]) -> ShapeType:
        """
        Classifica a forma baseada nos vértices simplificados.
        """
        if not vertices or len(vertices) < 3:
            return ShapeType.UNKNOWN

        poly = Polygon(vertices)
        if not poly.is_valid:
            poly = poly.buffer(0) # Tenta corrigir auto-interseções
            if not poly.is_valid:
                from shapely.validation import make_valid
                poly = make_valid(poly) # Correção mais agressiva se buffer(0) falhar

        simplified_poly = poly.simplify(0.01, preserve_topology=True)
        
        # Caso a simplificação gere múltiplos polígonos (artefatos), pegamos o maior
        if simplified_poly.geom_type == 'MultiPolygon':
            simplified_poly = max(simplified_poly.geoms, key=lambda p: p.area)
        
        if not hasattr(simplified_poly, 'exterior'):
            return ShapeType.UNKNOWN

        coords = list(simplified_poly.exterior.coords)
        if len(coords) > 0 and coords[0] == coords[-1]:
            coords.pop() # Remove ponto duplicado de fechamento
            
        num_vertices = len(coords)

        # Regras Heurísticas de Contagem de Vértices
        if num_vertices == 4:
            return ShapeType.RECTANGULAR
        elif num_vertices == 6:
            return ShapeType.L_SHAPE
        elif num_vertices == 8:
            # T vs U: Análise de Concavidade
            # T tem concavidades opostas (borda longa interrompida)
            # U tem concavidades no mesmo lado (boca do U)
            # Por simplicidade heurística inicial: Se bounding box preenchimento for alto > T, se baixo > U? Não confiável.
            # Vamos deixar T como default para 8 vertices por enquanto se não for claro.
            return ShapeType.T_SHAPE 
        elif num_vertices > 8:
            return ShapeType.CIRCULAR
        
        return ShapeType.UNKNOWN

    @staticmethod
    def _find_concave_vertex(vertices: List[Tuple[float, float]]) -> Optional[Tuple[float, float]]:
        """Identifica o vértice côncavo em um polígono L-Shape (único vértice interno)."""
        # Calcular produto vetorial (cross product) de arestas adjacentes
        n = len(vertices)
        for i in range(n):
            p1 = vertices[i-1]
            p2 = vertices[i]
            p3 = vertices[(i+1) % n]
            
            # Vetores
            v1 = (p2[0]-p1[0], p2[1]-p1[1])
            v2 = (p3[0]-p2[0], p3[1]-p2[1])
            
            cross = v1[0]*v2[1] - v1[1]*v2[0]
            
            # Assumindo polígono ordenado CCW (Counter-Clockwise), concavidade tem cross < 0
            # Se CW, cross > 0. Precisamos checar a orientação do polígono.
            # Polygon(vertices).area > 0 normalmente implica CCW no shapely se construído corretamente, mas inputs raw podem variar.
            # Vamos assumir que L-Shape tem 5 convexos e 1 côncavo. O diferente é o côncavo.
            pass
        
        # Abordagem agnóstica de ordem: 5 ângulos de um jeito, 1 do outro.
        angles = []
        for i in range(n):
            p1 = np.array(vertices[i-1])
            p2 = np.array(vertices[i])
            p3 = np.array(vertices[(i+1) % n])
            
            v1 = p1 - p2
            v2 = p3 - p2
            
            # Angulo entre vetores
            cosine_angle = np.dot(v1, v2) / (np.linalg.norm(v1) * np.linalg.norm(v2))
            angle = np.arccos(np.clip(cosine_angle, -1.0, 1.0))
            cross = v1[0]*v2[1] - v1[1]*v2[0]
            
            angles.append((i, cross))

        # Contar sinais
        pos = sum(1 for _, c in angles if c > 0)
        neg = sum(1 for _, c in angles if c < 0)
        
        target_sign = -1 if pos > neg else 1 # O minoritário é o côncavo
        
        for i, c in angles:
            if (c > 0 and target_sign == 1) or (c < 0 and target_sign == -1):
                return vertices[i]
        
        return None

    @staticmethod
    def map_sides(shape_type: ShapeType, rotation_degrees: float, vertices: List[Tuple[float, float]]) -> Dict[str, str]:
        """
        Mapeia os lados A-H.
        """
        sides = {}
        rot = rotation_degrees % 360

        if shape_type == ShapeType.RECTANGULAR:
            is_vertical = (45 <= rot <= 135) or (225 <= rot <= 315)
            if not is_vertical: # Horizontal
                sides = {'A': 'Baixo', 'B': 'Cima', 'C': 'Esq', 'D': 'Dir'}
            else: # Vertical
                sides = {'A': 'Esq', 'B': 'Dir', 'C': 'Cima', 'D': 'Baixo'}

        elif shape_type == ShapeType.L_SHAPE:
            # Detectar orientação do L baseado na posição do vértice côncavo relativo ao Bbox
            concave_pt = GeometryEngine._find_concave_vertex(vertices)
            if concave_pt:
                poly = Polygon(vertices)
                minx, miny, maxx, maxy = poly.bounds
                cx, cy = concave_pt
                
                # Normalizar posição (0.0 a 1.0)
                nx = (cx - minx) / (maxx - minx)
                ny = (cy - miny) / (maxy - miny)
                
                # Tolerância
                tol = 0.4
                
                # Lógica: Onde está a "quina interna"?
                # Se quina está Top-Right -> L normal (_|) -> Base em baixo, Pé na esq.
                # Se quina está Top-Left -> L invertido horiz (|_ ) -> Base em baixo, Pé na dir.
                # Se quina está Bottom-Right -> Teto em cima, Pé na esq (desce)
                
                # Mapping user rules:
                # "Pé do L à esquerda" = Formato _| (Quina Top-Right)
                # A=Esq(Pé), B=Dir(Pé), C=Cima(Pé). 
                # Base deitada -> E=Baixo, F=Cima(Base), D=Dir(Base).
                
                if nx > 0.5 and ny > 0.5: # Top-Right (Formato _|)
                    sides = {
                        'A': 'Pé Esq (Face Esq)', 'B': 'Pé Dir (Face Int vert)', 'C': 'Pé Cima (Topo)',
                        'E': 'Base Baixo', 'F': 'Base Cima (Face Int horiz)', 'D': 'Base Dir (Ponta)'
                    }
                elif nx < 0.5 and ny > 0.5: # Top-Left (Formato |_)
                    sides = {'Info': 'L Invertido Horizontal'}
                elif nx > 0.5 and ny < 0.5: # Bottom-Right (Formato -|)
                    sides = {'Info': 'L Invertido Vertical'}
                else: # Bottom-Left
                     sides = {'Info': 'L Rotacionado 180'}
            else:
                sides = {'Error': 'Geometria L inválida (sem concavidade)'}

        return sides

