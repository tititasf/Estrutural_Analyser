"""
Handler de erros para interface - captura erros e salva em log
"""
import os
import sys
import traceback
from datetime import datetime

def get_log_file():
    """Retorna o caminho do arquivo de log de erros"""
    if getattr(sys, 'frozen', False):
        log_dir = os.path.dirname(sys.executable)
    else:
        log_dir = os.path.dirname(os.path.abspath(__file__))
    return os.path.join(log_dir, "interface_errors.log")

def log_error(func_name, error, context=None):
    """Registra um erro no log"""
    try:
        log_file = get_log_file()
        with open(log_file, 'a', encoding='utf-8') as f:
            f.write(f"\n{'='*60}\n")
            f.write(f"Timestamp: {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}\n")
            f.write(f"Function: {func_name}\n")
            if context:
                f.write(f"Context: {context}\n")
            f.write(f"Error: {str(error)}\n")
            f.write(f"Traceback:\n{traceback.format_exc()}\n")
            f.write(f"{'='*60}\n")
    except Exception:
        pass  # Se não conseguir logar, ignora

def safe_execute(func, *args, **kwargs):
    """Executa uma função com tratamento de erros"""
    try:
        return func(*args, **kwargs)
    except Exception as e:
        log_error(func.__name__, e, f"Args: {args}, Kwargs: {kwargs}")
        raise

