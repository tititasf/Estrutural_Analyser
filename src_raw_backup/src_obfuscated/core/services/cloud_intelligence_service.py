import os
import shutil
import logging
from typing import Optional
from pathlib import Path
from src.core.infra.supabase_client import SupabaseClient

class CloudIntelligenceService:
    """
    Gerencia a atualização da Inteligência Artificial (laje_ai.db / structural_ai.db)
    via Supabase Storage. Permite 'hot-swap' das regras sem reinstalar o app.
    """
    
    BUCKET_NAME = "intelligence"
    
    def __init__(self, db_type: str = "structural"):
        """
        :param db_type: 'laje' ou 'structural'
        """
        self.client = SupabaseClient.get_instance().client
        self.logger = logging.getLogger(__name__)
        self.db_type = db_type
        
        if db_type == "laje":
            self.db_filename = "laje_ai.db"
        else:
            self.db_filename = "structural_ai.db"
            
        self.local_db_path = os.path.join(os.getcwd(), "data", self.db_filename)
        
        # Garantir diretório local
        os.makedirs(os.path.dirname(self.local_db_path), exist_ok=True)

    def check_for_updates(self, current_version: str) -> Optional[str]:
        """Verifica atualização para o tipo de banco atual."""
        try:
            # Busca version_laje.json ou version_structural.json no bucket
            version_file = f"version_{self.db_type}.json"
            data = self.client.storage.from_(self.BUCKET_NAME).download(version_file)
            import json
            remote_info = json.loads(data)
            remote_ver = remote_info.get("version", "0.0.0")
            
            if remote_ver != current_version:
                return remote_ver
            return None
        except Exception as e:
            self.logger.warning(f"No version info for {self.db_type}: {e}")
            return None

    def download_latest_intelligence(self, progress_callback=None) -> bool:
        """Downloads the configured database type."""
        try:
            self.logger.info(f"Downloading {self.db_filename}...")
            temp_path = self.local_db_path + ".tmp"
            
            with open(temp_path, 'wb') as f:
                res = self.client.storage.from_(self.BUCKET_NAME).download(self.db_filename)
                f.write(res)
                
            if os.path.exists(self.local_db_path):
                backup_path = self.local_db_path + ".bak"
                shutil.move(self.local_db_path, backup_path)
                
            shutil.move(temp_path, self.local_db_path)
            self.logger.info(f"{self.db_filename} updated.")
            
            if progress_callback:
                progress_callback(100, f"IA {self.db_type.capitalize()} Atualizada!")
            return True
        except Exception as e:
            self.logger.error(f"Error downloading {self.db_filename}: {e}")
            return False

    def get_local_db_path(self) -> str:
        return self.local_db_path
