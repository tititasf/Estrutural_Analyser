class RenderMode(Enum):
    """
    10 Modos de Renderização Consolidados (V10).
    Focados em Filtragem de Cor e Geometria de Engenharia.
    """
    TRUE_GEOMETRY = 1      # Modo 1: Bruto
    EDGE_CLEANER = 2       # Modo 2: Limpador de Ruído (Antigo 4)
    COLOR_BASIC = 3        # Modo 3: Cores 1-4 (Antigo 13 - Base de Sucesso)
    COLOR_EXTENDED = 4     # Modo 4: Cores 1-7 (Inclui Azul/Magenta/Branco)
    COLOR_LAYERS = 5       # Modo 5: Cores 1-4 + Whitelist de Camadas
    COLOR_ORTHO = 6        # Modo 6: Cores 1-4 + Ortogonalidade
    COLOR_FLATTEN = 7      # Modo 7: Cores 1-4 + Achatamento Z
    COLOR_BLOCKS = 8       # Modo 8: Cores 1-4 + Filtro Interno de Blocos
    COLOR_FAN = 9          # Modo 9: Cores 1-4 + Detector de Leques Global
    COLOR_OMEGA = 10       # Modo 10: OMEGA COLOR (Combinação Máxima)

class DXFLoader:
    def __init__(self, filepath: str = None, mode: RenderMode = RenderMode.TRUE_GEOMETRY):
        self.filepath = filepath
        self.mode = mode
        self.doc = None
        self.msp = None
        self.entities = {
            'polylines': [],
            'lines': [],
            'circles': [],
            'texts': [],
            'hatches': [],
            'blocks': [],
            'splines': [],
            'ellipses': [],
            'arcs': []
        }
        self.hidden_layers = set() # [NEW] Cache de camadas OFF ou FROZEN



    def load(self) -> bool:
        try:
            self.doc = ezdxf.readfile(self.filepath)
            self.msp = self.doc.modelspace()
            
            # [FIX] Mapear camadas ocultas ou congeladas
            self.hidden_layers = set()
            for layer in self.doc.layers:
                if not layer.is_off() or layer.is_frozen():
                    # Notas: ezdxf is_off() retorna True se desligada. 
                    # Mas queremos capturar o estado real de invisibilidade
                    pass
                if layer.dxf.get('plot', 1) == 0: # Não imprimível (comum em grelhas)
                     self.hidden_layers.add(layer.dxf.name.upper())
                if layer.is_off() or layer.is_frozen():
                     self.hidden_layers.add(layer.dxf.name.upper())

            self.entities = {
                'polylines': [],
                'lines': [],
                'circles': [],
                'texts': [],
                'hatches': [],
                'blocks': [],
                'splines': [],
                'ellipses': []
            }
            self._extract_entities(self.msp)
            
            # [FIX v7] Global Sanitizer: Executa após todo o carregamento (incluindo blocos)
            if self.mode != RenderMode.TRUE_GEOMETRY:
                self._global_purge()
                
            return True
        except Exception as e:
            logging.error(f"Failed to load DXF: {e}")
            return False

    def _extract_entities(self, container, override_layer=None, override_color=None, total_matrix: Matrix44 = None, is_block: bool = False):
        """Extrai entidades de um container (espaço ou bloco) de forma achatada."""
        
        # Garantir que o container suporte .query()
        if not hasattr(container, 'query'):
            from ezdxf.query import new as query_new
            container = query_new(container)
            
        def clean_mtext(text):
            import re
            # Remove códigos formatadores do MTEXT como \P, \H1.5;, \fArial; etc.
