            text = re.sub(r'\\P', '\n', text) # Nova linha
            text = re.sub(r'\\\{|\\\}', '', text) # Chaves de agrupamento
            text = re.sub(r'\\[a-zA-Z].*?;', '', text) # Códigos tipo \H, \f, \C
            text = re.sub(r'\{|\}', '', text) # Chaves remanescentes
            return text

