    def add_dxf_entities(self, entities, progress_callback=None, render_mode=RenderMode.TRUE_GEOMETRY, compute_snaps=True):
        """Renderiza geometria base (linhas, textos, etc) do DXF"""
        self.last_entities = entities # Cache para troca de estilo
        self.last_render_mode = render_mode
        self.scene.clear()
        
        # [MODO] ConfiguraГғВ§ГғВЈo baseada no modo de renderizaГғВ§ГғВЈo
        is_layout_exact = render_mode == RenderMode.EDGE_CLEANER
        self.compute_snaps = compute_snaps # Estado persistente para o worker interno se houver
        
        # Explicitly clear dictionaries to release Python references to C++ items
        self.snap_markers.clear() 
        self.interactive_items.clear()
        
        self.snap_markers = {} # Reset markers
        self.interactive_items = {}
        for k in self.item_groups: self.item_groups[k] = [] # Reset groups
        self.beam_visuals = []
        self.contour_visuals = []
        self.snap_points = []
        self.snap_points = []
        self.snap_segments = []
        
        # [NOVO] ГғВҚndices para filtragem instantГғВўnea
        self.filter_indices = {'layer': {}, 'color': {}, 'type': {}}
        self.dxf_metadata = {'layers': set(), 'colors': set(), 'types': set()}

        # -------------------------------------------------------------
        # OTIMIZAГғвҖЎГғЖ’O DE PERFORMANCE (BATCH LOADING)
        # -------------------------------------------------------------
        # Desabilitar atualizaГғВ§ГғВөes visuais e sinais durante a carga
        prev_update_mode = self.viewportUpdateMode()
        self.setViewportUpdateMode(QGraphicsView.NoViewportUpdate)
        self.scene.blockSignals(True)

        def _index_item(it, data, etype):
            if not it or not data: return
            lay = data.get('layer', '0')
            aci = data.get('aci', 256)
            self.filter_indices['layer'].setdefault(lay, []).append(it)
            self.filter_indices['color'].setdefault(aci, []).append(it)
            self.filter_indices['type'].setdefault(etype, []).append(it)
            self.dxf_metadata['layers'].add(lay)
            self.dxf_metadata['colors'].add(aci)
            self.dxf_metadata['types'].add(etype)
            # Vincular data ao item
            it.setData(0, data)

        # Consolidar todas as entidades para busca/picking
        self.dxf_entities = entities.get('texts', []).copy()
        for l in entities.get('lines', []):
            ent = l.copy()
            ent['points'] = [l['start'], l['end']] # Compatibilidade com busca de poly
            self.dxf_entities.append(ent)
        self.dxf_entities.extend(entities.get('polylines', []))
        self.dxf_entities.extend(entities.get('circles', []))
        
        total_steps = len(entities.get('lines', [])) + len(entities.get('polylines', [])) + \
                      len(entities.get('texts', [])) + len(entities.get('circles', []))
        current_step = 0

        def update_prog():
            nonlocal current_step
            current_step += 1
            if progress_callback and current_step % 50 == 0:
                progress_callback(int((current_step / total_steps) * 100))
        
        # Helper para criar Pen a partir de cor DXF
        def get_pen(color_tuple, width=1, dxf_lw=-3):
            if not color_tuple: color_tuple = (200, 200, 200)
            
            actual_width = width
            cosmetic = True
            
            # Se for Layout Exact, respeitar o lineweight do DXF
            # dxf_lw em 1/100mm. Ex: 50 = 0.5mm.
            if is_layout_exact and dxf_lw > 0:
                 actual_width = max(0.1, dxf_lw / 100.0) # mm no mundo real (cena)
                 cosmetic = False # Lineweights reais tГғВӘm dimensГғВЈo fГғВӯsica
            
            pen = QPen(QColor(*color_tuple), actual_width)
            pen.setCosmetic(cosmetic)
            return pen

        # Armazena linhas para cГғВЎlculo de interseГғВ§ГғВЈo
        calc_lines = []

        # Draw Circles and Arcs
        for circ in entities.get('circles', []):
            center = circ.get('center', circ.get('pos', (0,0)))
            cx, cy = center
            r = circ['radius']
            self._add_snap_point((cx, cy), 'center')
            self._add_snap_point((cx+r, cy), 'quadrant')
            self._add_snap_point((cx-r, cy), 'quadrant')
            self._add_snap_point((cx, cy+r), 'quadrant')
            self._add_snap_point((cx, cy-r), 'quadrant')

            if 'start_angle' in circ: # ARC
                path = QPainterPath()
                rect = QRectF(cx - r, cy - r, r * 2, r * 2)
                sa = circ['start_angle']
                ea = circ['end_angle']
                span = ea - sa
                if span < 0: span += 360
                path.arcMoveTo(rect, sa)
                path.arcTo(rect, sa, span)
                item = DXFPathItem(path)
                item.setPen(get_pen(circ['color'], dxf_lw=circ.get('lineweight', -3)))
                self.scene.addItem(item)
                item.setZValue(self.Z_BLOCK if circ.get('is_block') else self.Z_GEOMETRY)
                _index_item(item, circ, 'arc')
            else: # CIRCLE
                item = DXFEllipseItem(cx - r, cy - r, r * 2, r * 2)
                item.setPen(get_pen(circ['color'], dxf_lw=circ.get('lineweight', -3)))
                self.scene.addItem(item)
                item.setZValue(self.Z_BLOCK if circ.get('is_block') else self.Z_GEOMETRY)
                
            update_prog()

        # Draw Lines
        for line in entities.get('lines', []):
            s, e = line['start'], line['end']
            item = DXFLineItem(s[0], s[1], e[0], e[1])
            item.setPen(get_pen(line['color'], dxf_lw=line.get('lineweight', -3)))
            self.scene.addItem(item)
            item.setZValue(self.Z_BLOCK if line.get('is_block') else self.Z_LINE)
            item.setFlag(QGraphicsItem.ItemIsSelectable) 
            if self.compute_snaps:
                self.snap_segments.append((s, e))
                calc_lines.append(line)
                # Midpoint
                mid = ((s[0]+e[0])/2, (s[1]+e[1])/2)
                self._add_snap_point(mid, 'midpoint')
                self._add_snap_point(s, 'endpoint')
                self._add_snap_point(e, 'endpoint')
            _index_item(item, line, 'line')
            update_prog()

        # Draw Polylines
        for poly in entities.get('polylines', []):
            points = poly['points']
            if not points: continue
            
            path = QPainterPath()
            path.moveTo(points[0][0], points[0][1])
            if self.compute_snaps: self._add_snap_point(points[0], 'endpoint')
            
            for i in range(1, len(points)):
                p_prev = points[i-1]
                p_curr = points[i]
                path.lineTo(p_curr[0], p_curr[1])
                
                if self.compute_snaps:
                    # Segmento para interseГғВ§ГғВЈo e Midpoint
                    calc_lines.append({'start': p_prev, 'end': p_curr})
                    self.snap_segments.append((p_prev, p_curr))
                    mid = ((p_prev[0]+p_curr[0])/2, (p_prev[1]+p_curr[1])/2)
                    self._add_snap_point(mid, 'midpoint')
                    self._add_snap_point(p_curr, 'endpoint')
            
            if poly.get('closed'):
                path.closeSubpath()
                if self.compute_snaps:
                    p_last = points[-1]
                    p_first = points[0]
                    self.snap_segments.append((p_last, p_first))
                    calc_lines.append({'start': p_last, 'end': p_first})
                    mid = ((p_last[0]+p_first[0])/2, (p_last[1]+p_first[1])/2)
                    self._add_snap_point(mid, 'midpoint')
            
            item = DXFPathItem(path)
            item.setPen(get_pen(poly['color'], dxf_lw=poly.get('lineweight', -3)))
            self.scene.addItem(item)
            item.setZValue(self.Z_BLOCK if poly.get('is_block') else self.Z_LINE)
            item.setFlag(QGraphicsItem.ItemIsSelectable) 
            _index_item(item, poly, 'lwpolyline')
            update_prog()

        # [NOVO] Hatches
        for h in entities.get('hatches', []):
            path = QPainterPath()
            for pts in h.get('paths', []):
                if not pts: continue
                path.moveTo(pts[0][0], pts[0][1])
                for i in range(1, len(pts)):
                    path.lineTo(pts[i][0], pts[i][1])
                path.closeSubpath()
            
            item = DXFPathItem(path)
            # [AJUSTE] Hachuras "Normais": Preenchimento sГіlido semi-transparente
            # Isso esconde a triangulaГ§ГЈo interna e mostra como uma ГЎrea sГіlida.
            # Usamos Qt.NoPen para que as bordas dos triГўngulos internos nГЈo apareГ§am.
            it_color = QColor(*h['color'])
            it_color.setAlpha(120) # [REVERT] TransparГӘncia preferida pelo usuГЎrio
            item.setBrush(QBrush(it_color)) 
            # [CRITICAL] REMOVER QUALQUER PEN DA HACHURA. 
            item.setPen(Qt.NoPen) 
            
            self.scene.addItem(item)
            item.setZValue(self.Z_BLOCK if h.get('is_block') else self.Z_HATCH)
            item.setFlag(QGraphicsItem.ItemIsSelectable)
            _index_item(item, h, 'hatch')
            update_prog()
        # Calcular InterseГғВ§ГғВөes (OtimizaГғВ§ГғВЈo: Ignorar se houver muitos elementos ou se snaps desativados)
        if self.compute_snaps and len(calc_lines) < 1000:
            self._calculate_intersections(calc_lines)
        elif self.compute_snaps:
            print(f"Skipping intersection calculation for {len(calc_lines)} lines (Performance protection).")
            
        # [NOVO] Backup de Snaps do DXF para regeneraГғВ§ГғВЈo segura
        self.base_snap_points = self.snap_points.copy()
        self.base_snap_segments = self.snap_segments.copy()

        # Draw Texts
        from PySide6.QtWidgets import QGraphicsSimpleTextItem
        from PySide6.QtGui import QFont
        
        for txt in entities.get('texts', []):
            content = txt['text']
            pos_x, pos_y = txt['pos']
            
            # --- CUSTOM OFFSET LOGIC (Parafuso 25/75) ---
            if "P25" in content or "P75" in content:
                 pos_x += 10.0 
            
            # [AJUSTE ALINHAMENTO] Baseado em halign/valign do DXF
            # halign: 0=Left, 1=Center, 2=Right
            # valign: 0=Baseline, 1=Bottom, 2=Middle, 3=Top
            halign = txt.get('halign', 0)
            valign = txt.get('valign', 0)
            
            # [MOTOR DE TEXTO MULTI-ESTILO]
            style = getattr(self, 'text_style', 1)
            
            if style == 1: # MODO 1: Geometria Pura (Alta Fidelidade)
                item = QGraphicsSimpleTextItem(content)
                item.setBrush(QColor(*txt['color']))
                font_base_size = 100.0
                f = QFont("Arial")
                f.setPointSizeF(font_base_size)
                item.setFont(f)
                dxf_h = txt.get('height', 2.5)
                scale_factor = dxf_h / font_base_size
                transform = QTransform()
                wf = txt.get('width_factor', 1.0)
                transform.scale(scale_factor * wf, -scale_factor)
                rotation = txt.get('rotation', 0)
                if rotation: transform.rotate(-rotation)
                item.setTransform(transform)
                
            elif style == 2: # MODO 2: Outlined Path (Perfeito para Zoom)
                path = QPainterPath()
                f = QFont("Arial")
                dxf_h = txt.get('height', 2.5)
                f.setPointSizeF(dxf_h)
                path.addText(0, 0, f, content)
                item = self.scene.addPath(path, QPen(Qt.NoPen), QBrush(QColor(*txt['color'])))
                transform = QTransform()
                wf = txt.get('width_factor', 1.0)
                transform.scale(wf, -1)
                rotation = txt.get('rotation', 0)
                if rotation: transform.rotate(-rotation)
                item.setTransform(transform)
                
            elif style == 3: # MODO 3: Cosmetic Hybrid
                item = QGraphicsSimpleTextItem(content)
                item.setBrush(QColor(*txt['color']))
                f = QFont("Arial")
                f.setPointSizeF(9)
                item.setFont(f)
                item.setFlag(QGraphicsItem.ItemIgnoresTransformations)
                
            elif style == 4: # MODO 4: Lightweight
                item = QGraphicsSimpleTextItem(content)
                item.setBrush(QColor(*txt['color']))
                f = QFont("MS Sans Serif")
                f.setPointSizeF(txt.get('height', 2.5))
                item.setFont(f)
                item.scale(1, -1)
                
            elif style == 5: # MODO 5: Native CAD focus
                item = QGraphicsSimpleTextItem(content)
                item.setBrush(QColor(*txt['color']))
                f = QFont("Arial")
                f.setPixelSize(int(txt.get('height', 2.5) * 10))
                item.setFont(f)
                item.scale(0.1, -0.1)
                rotation = txt.get('rotation', 0)
                if rotation: item.setRotation(rotation)
                
            elif style == 6: # MODO 6: Normalized Auto (Fix agressivo)
                item = QGraphicsSimpleTextItem(content)
                item.setBrush(QColor(*txt['color']))
                # CAP mais agressivo: 0.5 unidades (Baseado no feedback de escala)
                orig_h = txt.get('height', 2.5)
                norm_h = min(orig_h, 0.5) 
                f = QFont("Arial")
                f.setPointSizeF(norm_h)
                item.setFont(f)
                item.scale(1, -1)
                rotation = txt.get('rotation', 0)
                if rotation: item.setRotation(rotation)

            # [NOVO] Alinhamento Robusto e Origem de RotaГғВ§ГғВЈo
            # Garantir que textos rodem em torno do seu ponto de ancoragem CAD
            brect = item.boundingRect()
            
            # Definir ponto de ancoragem local (ГғВўncora do CAD)
            anchor_x = 0
            anchor_y = 0
            if halign == 1: anchor_x = brect.width() / 2
            elif halign == 2: anchor_x = brect.width()
            
            if valign == 2: anchor_y = brect.height() / 2 # Middle
            elif valign == 3: anchor_y = brect.height()   # Top
            
            # Configurar origem de transformaГғВ§ГғВЈo para a ГғВўncora
            item.setTransformOriginPoint(anchor_x, anchor_y)
            
            # Se for um estilo simples (3, 4, 5, 6) que usa rotation direta:
            rotation = txt.get('rotation', 0)
            if style in (3, 4, 5, 6) and rotation:
                item.setRotation(rotation)
                
            # Posicionamento Final: O ponto (pos_x, pos_y) do CAD deve coincidir com a ГғВўncora
            # Por padrГғВЈo, setPos coloca o (0,0) local na coordenada. 
            # Precisamos recuar a posiГғВ§ГғВЈo do item para que a ГғВўncora caia no (pos_x, pos_y).
            item.setPos(pos_x - anchor_x, pos_y - anchor_y)
                
            item.setFlag(QGraphicsItem.ItemIsSelectable)
            self.scene.addItem(item)
            item.setZValue(self.Z_BLOCK if txt.get('is_block') else self.Z_TEXT)
            self._add_snap_point((pos_x, pos_y), 'node')
            _index_item(item, txt, 'text')
            update_prog()
            
        # [NOVO] Ellipses
        for ell in entities.get('ellipses', []):
            cx, cy = ell['center']
            rx, ry = ell['rx'], ell['ry']
            rot = ell.get('rotation', 0)
            
            # Usar QGraphicsEllipseItem e rotacionar via transform
            rect = QRectF(-rx, -ry, rx * 2, ry * 2)
            item = DXFEllipseItem(rect.x(), rect.y(), rect.width(), rect.height())
            
            transform = QTransform()
            transform.translate(cx, cy)
            if rot: transform.rotate(-rot)
            item.setTransform(transform)
            
            item.setPen(get_pen(ell['color'], dxf_lw=ell.get('lineweight', -3)))
            self.scene.addItem(item)
            item.setZValue(self.Z_BLOCK if ell.get('is_block') else self.Z_GEOMETRY)
            _index_item(item, ell, 'ellipse')
            update_prog()
            
        # [NOVO] Splines
        for spl in entities.get('splines', []):
            points = spl.get('points', [])
            if not points: continue
            
            path = QPainterPath()
            path.moveTo(points[0][0], points[0][1])
            for i in range(1, len(points)):
                path.lineTo(points[i][0], points[i][1])
                
            item = DXFPathItem(path)
            item.setPen(get_pen(spl['color'], dxf_lw=spl.get('lineweight', -3)))
            self.scene.addItem(item)
            item.setZValue(self.Z_BLOCK if spl.get('is_block') else self.Z_LINE)
            _index_item(item, spl, 'spline')
            update_prog()
            
        self._init_osnap_markers()
        self._init_instruction_overlay()
        
        # [OTIMIZAГғвҖЎГғЖ’O ZOOM] Smart View 2.0 (Foco no Objeto Real)
        pts = []
        # Coletar pontos de geometria (Lines, Polylines, Circles) - Estes definem o desenho
        for l in entities.get('lines', []): 
            pts.append(l['start']); pts.append(l['end'])
        for p in entities.get('polylines', []):
            for pt in p.get('points', []): pts.append(pt)
        for c in entities.get('circles', []):
            center = c.get('center', c.get('pos'))
            if center: pts.append(center)
            
        # Coletar textos apenas se houver poucos ou como secundГғВЎrio
        # (Se o projeto tiver 1 trilhГғВЈo de textos no 0,0 eles nГғВЈo devem estragar o zoom)
        txt_pts = [t['pos'] for t in entities.get('texts', [])]
        
        # Filtro de Outliers por Percentil (5% a 95%)
        all_pts = pts + txt_pts
        if not all_pts: all_pts = [(0,0), (100,100)] # Fallback
        
        xs = sorted([p[0] for p in all_pts])
        ys = sorted([p[1] for p in all_pts])
        
        p5 = int(len(xs) * 0.05)
        p95 = int(len(xs) * 0.95)
        
        if p95 > p5:
            xmin, xmax = xs[p5], xs[p95]
            ymin, ymax = ys[p5], ys[p95]
            
            # Garantir que o retГғВўngulo nГғВЈo seja degenerado
            width = max(1.0, xmax - xmin)
            height = max(1.0, ymax - ymin)
            rect = QRectF(xmin, ymin, width, height)
            
            # UX: Margem de 10% do tamanho do desenho (muito melhor que margem fixa)
            margin_x = width * 0.1
            margin_y = height * 0.1
            rect = rect.adjusted(-margin_x, -margin_y, margin_x, margin_y)
            
            self.setSceneRect(rect)
            self.fitInView(rect, Qt.KeepAspectRatio)
        else:
            self.fitInView(self.scene.itemsBoundingRect(), Qt.KeepAspectRatio)

        # -------------------------------------------------------------
        # RESTAURAR ESTADO APГғвҖңS BATCH LOADING
        # -------------------------------------------------------------
        self.scene.blockSignals(False)
        self.setViewportUpdateMode(prev_update_mode)
        self.update() # ForГғВ§ar repaint final

    def _init_osnap_markers(self):
        """Prepara os marcadores de snap (Quadrado, TriГғВўngulo, X)"""
        # Endpoint/Center: Quadrado Verde
        sq = self.scene.addRect(-5, -5, 10, 10, QPen(QColor(0, 255, 0), 2))
        sq.setZValue(200); sq.hide(); sq.setFlag(QGraphicsItem.ItemIgnoresTransformations)
        self.snap_markers['endpoint'] = sq
        self.snap_markers['center'] = sq # Reutiliza
        self.snap_markers['quadrant'] = sq # Reutiliza
        
        # Node: CГғВӯrculo Verde com X
        node_path = QPainterPath()
        node_path.addEllipse(-4, -4, 8, 8)
        node_path.moveTo(-3, -3); node_path.lineTo(3, 3)
        node_path.moveTo(3, -3); node_path.lineTo(-3, 3)
        node = self.scene.addPath(node_path, QPen(QColor(0, 255, 0), 1))
        node.setZValue(200); node.hide(); node.setFlag(QGraphicsItem.ItemIgnoresTransformations)
        self.snap_markers['node'] = node

        # Midpoint: TriГғВўngulo Ciano
        from PySide6.QtGui import QPolygonF
        from PySide6.QtCore import QPointF
        tri_poly = QPolygonF([QPointF(0, -6), QPointF(-5, 4), QPointF(5, 4)])
        tri = self.scene.addPolygon(tri_poly, QPen(QColor(0, 255, 255), 2))
        tri.setZValue(200); tri.hide(); tri.setFlag(QGraphicsItem.ItemIgnoresTransformations)
        self.snap_markers['midpoint'] = tri

        # Intersection: X Vermelho/Laranja
        path = QPainterPath()
        path.moveTo(-5, -5); path.lineTo(5, 5)
        path.moveTo(5, -5); path.lineTo(-5, 5)
        cross = self.scene.addPath(path, QPen(QColor(255, 100, 0), 2))
        cross.setZValue(200); cross.hide(); cross.setFlag(QGraphicsItem.ItemIgnoresTransformations)
        self.snap_markers['intersection'] = cross

        # Nearest: Ampulheta/X (Ampulheta simplificada)
        path = QPainterPath()
        path.moveTo(-4, -4); path.lineTo(4, 4); path.lineTo(-4, 4); path.lineTo(4, -4); path.closeSubpath()
        near = self.scene.addPath(path, QPen(QColor(0, 255, 0), 1))
        near.setZValue(200); near.hide(); near.setFlag(QGraphicsItem.ItemIgnoresTransformations)
        self.snap_markers['nearest'] = near

        # Perpendicular: SГғВӯmbolo de ГғВўngulo reto
        path = QPainterPath()
        path.moveTo(0, -6); path.lineTo(0, 0); path.lineTo(6, 0)
        perp = self.scene.addPath(path, QPen(QColor(0, 255, 0), 2))
        perp.setZValue(200); perp.hide(); perp.setFlag(QGraphicsItem.ItemIgnoresTransformations)
        self.snap_markers['perpendicular'] = perp

        # Extension: X tracejado ou pequeno circulo
        ext_path = QPainterPath()
        ext_path.moveTo(-4, 0); ext_path.lineTo(4, 0)
        ext_path.moveTo(0, -4); ext_path.lineTo(0, 4)
        ext = self.scene.addPath(ext_path, QPen(QColor(0, 255, 0), 1, Qt.DashLine))
        ext.setZValue(200); ext.hide(); ext.setFlag(QGraphicsItem.ItemIgnoresTransformations)
        self.snap_markers['extension'] = ext

