            # Zoom to it with consistent margin (400px agradГғВЎvel)
            if apply_zoom:
                rect = item.sceneBoundingRect()
                margin = 400
                self.fitInView(rect.adjusted(-margin, -margin, margin, margin), Qt.KeepAspectRatio)
                self.centerOn(rect.center())
            
