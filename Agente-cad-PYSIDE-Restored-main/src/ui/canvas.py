    def _get_best_entity_under_cursor(self, scene_pos, mode='geometry', aperture=10.0):
        """Busca a melhor entidade DXF ou interativa sob o cursor para picking"""
        # 1. Tentar primeiro por Itens Reais na Cena (Mais preciso para textos da interface)
        view_scale = self.transform().m11()
        # [USER REQUEST] Extreme precision (1px scale)
        search_aperture = 1 / (view_scale if view_scale > 0 else 1)
        rect = QRectF(scene_pos.x() - search_aperture/2, scene_pos.y() - search_aperture/2, search_aperture, search_aperture)
        items = self.scene.items(rect, Qt.IntersectsItemShape)
        
        valid_candidates = []
        for i in items:
            if mode == 'text' and isinstance(i, QGraphicsSimpleTextItem):
                return {'text': i.text(), 'type': 'text', 'pos': (i.pos().x(), i.pos().y())}
            if mode == 'geometry':
                if isinstance(i, (PillarGraphicsItem, SlabGraphicsItem, BeamGraphicsItem, DXFLineItem, DXFPathItem, DXFEllipseItem)):
                    valid_candidates.append(i)
        
        if valid_candidates:
            closest = self._get_closest_item(scene_pos, valid_candidates)
            if closest:
                if isinstance(closest, (PillarGraphicsItem, SlabGraphicsItem, BeamGraphicsItem)):
                    return {'text': closest.item_data.get('name', 'Item'), 'type': 'geometry', 'id': closest.item_data.get('id')}
                if isinstance(closest, DXFLineItem):
                    p1 = (closest.line().p1().x(), closest.line().p1().y())
                    p2 = (closest.line().p2().x(), closest.line().p2().y())
                    return {'text': f"{closest.line().length():.1f}", 'type': 'line', 'points': [p1, p2]}
                return {'text': 'Entidade CAD', 'type': 'geometry', 'pos': (scene_pos.x(), scene_pos.y())}

        # 2. Tentar por Proximidade nos Meta-Dados DXF (Fallback)
        best_ent = None
        min_dist = aperture
        
        for ent in self.dxf_entities:
            dist = 9999.0
            if mode == 'text':
                if 'text' in ent:
                    p = ent['pos']
                    dist = ((p[0]-scene_pos.x())**2 + (p[1]-scene_pos.y())**2)**0.5
            else: # geometry / all
                if 'points' in ent:
                    for p in ent['points']:
                        d = ((p[0]-scene_pos.x())**2 + (p[1]-scene_pos.y())**2)**0.5
                        if d < dist: dist = d
                elif 'pos' in ent and 'text' in ent:
                    p = ent['pos']
                    dist = ((p[0]-scene_pos.x())**2 + (p[1]-scene_pos.y())**2)**0.5
                elif 'radius' in ent and ('pos' in ent or 'center' in ent):
                    p = ent.get('pos', ent.get('center'))
                    d_center = ((p[0]-scene_pos.x())**2 + (p[1]-scene_pos.y())**2)**0.5
                    dist = abs(d_center - ent['radius'])

            if dist < min_dist:
                min_dist = dist
                best_ent = ent

        if best_ent:
            return {
                'type': best_ent.get('type', 'geometry'),
                'text': best_ent.get('text', 'Entidade CAD'),
                'pos': best_ent.get('pos', best_ent.get('center')),
                'points': best_ent.get('points'),
                'radius': best_ent.get('radius')
            }
        return None

    def select_similar(self):
        """Seleciona todos os itens que compartilham camada e tipo com o ГғВәltimo item selecionado"""
        if not self.selected_items:
            print("Nenhum item selecionado para buscar similares.")
            return
            
        target = self.selected_items[-1]
        
        # [NEW] VerificaГ§ГЈo de validade do objeto C++ (Crash Protection)
        try:
            target_layer = getattr(target, 'dxf_layer', None)
            target_type = type(target)
        except RuntimeError:
            print("DEBUG: Target item was deleted. Cleaning list.")
            self.selected_items = [i for i in self.selected_items if i != target]
            return
        
        # Filtro de seguranГ§a para nГЈo selecionar itens de sistema (markers, etc)
        if isinstance(target, (QGraphicsEllipseItem, QGraphicsPathItem, QGraphicsLineItem, QGraphicsSimpleTextItem)) or \
           isinstance(target, (DXFLineItem, DXFPathItem, DXFEllipseItem, PillarGraphicsItem, SlabGraphicsItem, BeamGraphicsItem)):
            
            count = 0
            # Usar scene.items() para buscar globalmente
            for item in self.scene.items():
                if isinstance(item, target_type):
                    item_layer = getattr(item, 'dxf_layer', None)
                    if item_layer == target_layer:
                        if not item.isSelected():
                            item.setSelected(True)
                            if item not in self.selected_items:
                                self.selected_items.append(item)
                            count += 1
            
            if count > 0:
                print(f"Selecionados {count} itens similares.")
                self._on_selection_changed()
        else:
            print("Item selecionado nГғВЈo ГғВ© compatГғВӯvel para busca de similares.")

    def wheelEvent(self, event):
        """Zoom in/out com scroll do mouse"""
        zoom_in_factor = 1.15
        zoom_out_factor = 1 / zoom_in_factor

        # Se houver scroll vertical
        if event.angleDelta().y() > 0:
            zoom_factor = zoom_in_factor
        else:
            zoom_factor = zoom_out_factor

        self.scale(zoom_factor, zoom_factor)
        
        # Ignora propagaГғВ§ГғВЈo (evita scroll das barras)
        event.accept()
